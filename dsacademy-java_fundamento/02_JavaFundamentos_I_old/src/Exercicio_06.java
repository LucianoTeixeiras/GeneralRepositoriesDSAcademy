public class Exercicio_06 {

    // Operadores Relacionhais e de Igualdade

    public static void main(String[] args) {

        int x = 10, y = 5;

        System.out.println("x > y : " + (x > y));
        System.out.println("x < y : " + (x < y));
        System.out.println("x >= y : " + (x >= y));
        System.out.println("x <= y : " + (x <= y));
        System.out.println("x == y : " + (x == y));
        System.out.println("x != y : " + (x != y));

    }

}
